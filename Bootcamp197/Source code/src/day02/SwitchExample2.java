package day02;

public class SwitchExample2 {

	public static void main(String[] args) {
		int num= 20;
		
		switch (num) {
		case 10:
			System.out.println("10");
			
		case 20:
			System.out.println("20");
			
		case 30:
			System.out.println("30");
			
			default:
				System.out.println("not in 10, 20, 30");
		}

	}

}
