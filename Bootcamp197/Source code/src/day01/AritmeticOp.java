package day01;

public class AritmeticOp {
			public static void main (String[] args)  {
			int a = 10;
			int b = 20;
			int d = 25;
			int c = 25;
			
			System.out.println("a + b = " + (a + b));
			System.out.println("a - b = " + (a - b));
			System.out.println("a * b = " + (a * b));
			System.out.println("b / a = " + (b / a));
			System.out.println("b % a = " + (b % a));
			System.out.println("c % a = " + (c % a));
			System.out.println("a++   = " + (a++));
			System.out.println("a--   = " + (a--));
			
			System.out.println("d++   = " + (d++));
			System.out.println("++d   = " + (++d));
			
		
		}
}
